#!/usr/bin/env python3
"""T2.5 — One-shot "trained adapter -> public model" publisher.

Takes a directory holding a trained LoRA adapter (the output of
workers/distill/scripts/train_lora.py) plus its training manifest + eval
summary and turns it into something the world can download and run:

  1. GGUF  — convert the (merged) HF weights to GGUF + quantize to one or more
             llama.cpp k-quants (Q4_K_M, Q5_K_M, ...). Opt-in via --quants; the
             tooling (llama.cpp's convert_hf_to_gguf.py + the llama-quantize
             binary) is located on PATH / $KOLM_LLAMACPP_DIR. When it is NOT
             present the run does NOT fail: the summary carries a
             "gguf_tooling_unavailable" status and we still emit the model card
             + Modelfile + (optionally) push the safetensors.
  2. README — a HuggingFace Hub model card, generated by REUSING the existing
             src/hf-modelcard.js writeModelCard() (spawned via `node`). The
             passport.json + benchmark JSON in the adapter dir drive it. We do
             NOT reinvent the card here.
  3. Modelfile — an Ollama Modelfile pointing at the primary GGUF, written
             locally so `ollama create` (or a human) can consume it.
  4. HF push — opt-in via --publish + --hf <user/repo>. Requires the HF_TOKEN
             env var (preflighted; missing -> exit 2 before any network call).
  5. Ollama — opt-in via --ollama <name>; runs `ollama create <name> -f
             <Modelfile>` only when the ollama binary is on PATH.

SAFETY (opt-in by design):
  - Default behaviour (no --publish, no --ollama) is a DRY RUN: produce the
    README.md + Modelfile (+ GGUF if tooling present) locally, touch no
    network and no ollama daemon.
  - HF/Ollama side effects fire ONLY behind their explicit flags.

Output: a machine-readable JSON summary is printed as the LAST stdout line.
Human-facing progress goes to stderr.

Exit codes:
  0  ok (dry-run or a real publish that completed)
  2  bad args OR a required dependency / preflight is missing
     (e.g. --publish without HF_TOKEN, or --quants without a model dir)
  3  a required input path is missing (the adapter dir / its contents)
  4  a runtime / network step raised

Heavy + optional deps (huggingface_hub, the gguf tooling, the ollama binary)
are imported / resolved lazily INSIDE functions, so `--help` and `--dry-run`
work on a box with none of them installed.
"""

