// =============================================================================
// PUBLIC: mapCardToGovernancePlatform(card, platform)
//
// Remap a kolm card envelope into the platform's expected field-name shape.
// Returns:
//   { ok: true, platform, version, mapped: { ... }, mapping_used: { ... } }
// or
//   { ok: false, error: 'unknown_platform', supported: [...] }
//
// We do NOT mutate the input card. We return a NEW object whose keys are
// platform-namespaced. Unmapped sections are dropped from the output (the
// platform doesn't have a slot for them) - we surface them in
// `unmapped_sections` so the integrator can wire them as custom fields.
// =============================================================================
export function mapCardToGovernancePlatform(card, platform) {
  if (typeof platform !== 'string' || !platform) {
    return {
      ok: false,
      error: 'platform_required',
      supported: SUPPORTED_GOVERNANCE_PLATFORMS,
      version: MODEL_CARD_SCHEMA_VERSION,
    };
  }
  const mapping = GOVERNANCE_PLATFORM_MAPPINGS[platform];
  if (!mapping) {
    return {
      ok: false,
      error: 'unknown_platform',
      hint: 'supported: ' + SUPPORTED_GOVERNANCE_PLATFORMS.join(', '),
      supported: SUPPORTED_GOVERNANCE_PLATFORMS,
      version: MODEL_CARD_SCHEMA_VERSION,
    };
  }
  const src = (card && typeof card === 'object') ? card : {};
  const mapped = {};
  const unmapped = [];
  for (const [kolmField, value] of Object.entries(src)) {
    const platformField = mapping[kolmField];
    if (typeof platformField === 'string' && platformField) {
      mapped[platformField] = value;
    } else {
      unmapped.push(kolmField);
    }
  }
  return {
    ok: true,
    platform,
    version: MODEL_CARD_SCHEMA_VERSION,
    mapped,
    mapping_used: mapping,
    unmapped_sections: unmapped,
  };
}

