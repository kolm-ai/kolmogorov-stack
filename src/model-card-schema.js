// W768 - Model Card schema + governance-platform mappings.
//
// Spec (KOLM_W707_SYSTEM_UPGRADE_PLAN.md lines 603-607):
//   [W768-3] Embeddable in OneTrust / ServiceNow AI Governance / IBM OpenPages
//
// Carries:
//   - MODEL_CARD_JSON_SCHEMA: a real JSON Schema draft-07 document describing
//     the HF Model Card v0.3 shape we emit (10 sections, typed properties).
//   - GOVERNANCE_PLATFORM_MAPPINGS: frozen lookup of kolm-card-field ->
//     downstream-platform-field for the three named GRC suites.
//   - mapCardToGovernancePlatform(card, platform): pure function that
//     remaps a kolm card envelope into a platform-specific shape.
//
// Why a SEPARATE file from model-card-emit.js:
//   - The schema is a public artifact; consumers (governance teams)
//     reach for it independently of the runtime emitter and we want a
//     small import surface that does NOT pull in the rest of the
//     emitter logic.
//   - The mappings are reference data, not code. Keeping them frozen
//     in one place makes the audit story trivial.
//
// HONESTY CONTRACT:
//   - Where a platform's exact field path is publicly documented, we use
//     it verbatim (e.g. OneTrust 'aiInventory.modelMetadata' for the AI
//     Inventory product). Where the platform exposes a customer-defined
//     custom-field surface we use a clearly-namespaced placeholder
//     (e.g. 'custom.intendedUse') so an integrator knows to wire it.
//   - mapCardToGovernancePlatform returns an honest ok:false envelope
//     for unknown platforms; it NEVER substitutes a different platform's
//     mapping silently.

